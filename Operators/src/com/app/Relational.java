package com.app;

public class Relational {
	public static void main(String[] args) {
			
			int a=50;
			int b=50;
			boolean result;
			
			 result=a>b;  //true
			 System.out.println(" > "+result);
			 result=a<b;  //false
			 System.out.println(" < "+result);
			 result=a>=b;  //true
			 System.out.println(" >= "+result);
			 result=a<=b;  //false
			 System.out.println(" <= "+result);
			 result=a==b;  //fasle
			 System.out.println(" == "+result);
			 result=a!=b;  //true
			 System.out.println(" != "+result);
		}

	}
	